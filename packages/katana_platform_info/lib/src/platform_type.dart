/// A type that represents the platform type.
///
/// プラットフォームの種類を表す型。
enum PlatformType {
  /// The platform is Web.
  ///
  /// プラットフォームがWebであることを表す。
  web,

  /// The platform is Windows.
  ///
  /// プラットフォームがWindowsであることを表す。
  windows,

  /// The platform is Linux.
  ///
  /// プラットフォームがLinuxであることを表す。
  linux,

  /// The platform is macOS.
  ///
  /// プラットフォームがmacOSであることを表す。
  macOS,

  /// The platform is Android.
  ///
  /// プラットフォームがAndroidであることを表す。
  android,

  /// The platform is Fuchsia.
  ///
  /// プラットフォームがFuchsiaであることを表す。
  fuchsia,

  /// The platform is iOS.
  ///
  /// プラットフォームがiOSであることを表す。
  iOS;
}
